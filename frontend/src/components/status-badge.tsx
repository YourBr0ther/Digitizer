import { JobStatus } from "@/lib/types";

const styles: Record<JobStatus, string> = {
  complete: "bg-emerald-500/15 text-emerald-400 border-emerald-500/30",
  failed: "bg-red-500/15 text-red-400 border-red-500/30",
  ripping: "bg-amber-500/15 text-amber-400 border-amber-500/30",
  detected: "bg-blue-500/15 text-blue-400 border-blue-500/30",
};

export default function StatusBadge({ status }: { status: JobStatus }) {
  return (
    <span
      className={`inline-flex items-center px-2 py-0.5 text-xs font-medium rounded border ${
        styles[status] || "bg-gray-500/15 text-gray-400 border-gray-500/30"
      }`}
    >
      {status}
    </span>
  );
}
